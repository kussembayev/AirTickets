import UIKit

extension UIColor {
    
    class func navBar() -> UIColor {
        return UIColor(red: 17/255.0, green: 83/255.0, blue: 211/255.0, alpha: 1.0)
    }
    
    class func navBarLight() -> UIColor {
        return UIColor.white
    }
    
    class func tabBar() -> UIColor {
        return UIColor.white
    }
    
    class func bg() -> UIColor {
        return UIColor(red: 17/255.0, green: 83/255.0, blue: 211/255.0, alpha: 1.0)
    }
}

